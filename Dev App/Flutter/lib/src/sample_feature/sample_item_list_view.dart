                  title: Text('${items[index].id}'),
                  leading: const CircleAvatar(
                    foregroundImage:
                        AssetImage('assets/images/flutter_logo.png'),
                  ),
                  onTap: () {
                    Navigator.restorablePushNamed(
                        context, IpItemDetailsView.routeName,
                        arguments: items[index].type);
                  });
            }
          }
        },
      ),
    );
  }
